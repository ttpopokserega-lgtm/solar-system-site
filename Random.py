# Імпортуємо бібліотеку для роботи з випадковими числами
import random

# Введення кількості чисел та діапазону
n = int(input("Введіть кількість чисел: "))
a = int(input("Введіть початок діапазону: "))
b = int(input("Введіть кінець діапазону: "))

# Генеруємо та виводимо числа
numbers = [random.randint(a, b) for і in range(n)]
print("Згенеровані числа:", numbers)

# Знаходимо та виводимо найбільше число
max_number = max(numbers)
print("Найбільше число:", max_number)
